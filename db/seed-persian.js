async function seed() {
  const client = await pool.connect();
  try {
    await client.query('BEGIN');

    // Insert categories
    for (const cat of categories) {
      await client.query(
        'INSERT INTO categories (profile_id, name, emoji) VALUES ($1, $2, $3) ON CONFLICT (profile_id, name) DO NOTHING',
        [PROFILE, cat.name, cat.emoji]
      );
    }

    // Get category IDs
    const catRows = await client.query(
      "SELECT id, name FROM categories WHERE profile_id = $1",
      [PROFILE]
    );
    const catMap = {};
    catRows.rows.forEach(r => { catMap[r.name] = r.id; });

    // Insert cards
    let inserted = 0;
    for (const card of cards) {
      const catId = catMap[card.cat];
      if (!catId) {
        console.warn(`Category not found: ${card.cat}`);
        continue;
      }

      const res = await client.query(
        `INSERT INTO cards (category_id, dutch, english, farsi_script, example_nl, example_en)
         VALUES ($1, $2, $3, $4, $5, $6)
         ON CONFLICT DO NOTHING
         RETURNING id`,
        [catId, card.dutch, card.english, card.farsi_script, card.example_nl || null, card.example_en || null]
      );

      if (res.rows.length > 0) {
        await client.query(
          'INSERT INTO progress (card_id) VALUES ($1) ON CONFLICT (card_id) DO NOTHING',
          [res.rows[0].id]
        );
        inserted++;
      }
    }

    await client.query('COMMIT');
    console.log(`Seeded ${categories.length} Persian categories and ${inserted} cards!`);
  } catch (err) {
    await client.query('ROLLBACK');
    throw err;
  } finally {
    client.release();
    await pool.end();
  }
}

seed().catch(err => {
  console.error('Error seeding Persian data:', err);
  process.exit(1);
});
